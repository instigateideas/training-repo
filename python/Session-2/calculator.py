

class Calculator():
    def __init__(self, x, y):
        self.x = x
        self.y = y
    
    def add(self):
        return self.x+self.y
    
    def subract(self):
        return self.x-self.y
    
    def mult(self):
        return self.x*self.y
    
    def division(self):
        return self.x/self.y
